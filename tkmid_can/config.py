"""
CAN 总线配置常量
================
CAN ID 定义、档位枚举、总线默认参数等。
所有模块均可独立导入此文件，无需依赖其他子模块。
"""

from enum import IntEnum

# ============================================================
# CAN ID 定义
# ============================================================
CTRL_CMD_ID      = 0x18C4D1D0   # 运动控制指令-控制帧
FREE_CTRL_CMD_ID = 0x18C4D2D0   # 自由控制指令-控制帧
IO_CMD_ID        = 0x18C4D7D0   # IO 控制指令-控制帧
CTRL_FB_ID       = 0x18C4D1EF   # 运动控制状态-反馈帧
L_WHEEL_FB_ID    = 0x18C4D7EF   # 左轮控制状态-反馈帧
R_WHEEL_FB_ID    = 0x18C4D8EF   # 右轮控制状态-反馈帧
IO_FB_ID         = 0x18C4DAEF   # IO 控制状态-反馈帧
BMS_FB_ID        = 0x18C4E1EF   # 电池状态-反馈帧
BMS_FLAG_FB_ID   = 0x18C4E2EF   # 电池标志状态-反馈帧

# ============================================================
# 档位枚举
# ============================================================
class Gear(IntEnum):
    DISABLE     = 0   # 禁用
    PARK        = 1   # 驻车档
    NEUTRAL     = 2   # 空档
    MOTION_CTRL = 3   # 运动学控制档
    FREE_CTRL   = 4   # 自由控制档

# ============================================================
# 安全限幅
# ============================================================
MAX_SPEED        = 2.0     # 线速度最大值 m/s
MAX_ANGULAR_VEL  = 100.0   # 角速度最大值 °/s

# ============================================================
# 总线默认参数
# ============================================================
DEFAULT_CHANNEL = 'can0'
DEFAULT_BUSTYPE = 'socketcan'
DEFAULT_BITRATE = 500000
SEND_PERIOD     = 0.01    # 发送周期 10ms
IO_FB_PERIOD    = 0.05    # IO 反馈周期 50ms
